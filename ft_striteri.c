#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	size_t	o;

	o = 0;
	if (s && f)
	{
		while (s[o])
		{
			f(o, &s[o]);
			o++;
		}
	}
}
